// 与原 cnGeo.cityJitter 完全一致的确定性抖动（±~3km）
function cityJitter(id) {
  let h = 0;
  for (let i = 0; i < id.length; i++) h = (h * 31 + id.charCodeAt(i)) | 0;
  const a = (h % 360) * (Math.PI / 180);
  const r = ((Math.abs(h >> 9) % 100) / 100) * 0.03;
  return [Math.cos(a) * r, Math.sin(a) * r];
}

// 解析一个台名 → { city, lng, lat, tier }
function locate(name, id) {
  // 1) 城市优先（先长后短，避免「广」误伤）
  for (const c of CITY_NAMES) {
    if (name.includes(c)) {
      const [lng, lat] = CITIES[c];
      return { city: c, lng, lat, tier: "city" };
    }
  }
  // 2) 手工覆盖（语言/全国台）
  for (const [kw, [lng, lat], label] of OVERRIDES) {
    if (name.includes(kw)) return { city: label, lng, lat, tier: "override" };
  }
  // 3) 省份兜底（落省会）
  for (const p of PROVINCE_NAMES) {
    if (name.includes(p)) {
      const [lng, lat] = PROVINCES[p];
      return { city: p, lng, lat, tier: "province" };
    }
  }
  // 4) 实在无法定位 → 北京中心（极少数）
  return { city: "全国", lng: NATIONAL_CENTER[0], lat: NATIONAL_CENTER[1], tier: "fallback" };
}

